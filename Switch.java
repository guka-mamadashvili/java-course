public class Switch {
    public static void main(String[] args) {
	char letter = 'n';

	switch (letter) {
	    case 'a':
		System.out.println("Nah , dont care!");
		break;
	    case 'm':
		System.out.println("Nah, dont care");
		break;
	    case 'n':
		System.out.println("Yes, N is the best!");
		break;
	}
    }
}
